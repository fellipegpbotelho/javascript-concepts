/* MATH API */

var number = 10.25;

console.log(Math.floor(number)); // 10
console.log(Math.ceil(number)); // 11
console.log(Math.round(number)); // 10
console.log(Math.pow(number, 2)); // 105.0625
console.log(Math.cos(1)); // 0.5403023058681398
console.log(Math.sin(1)); // 0.8414709848078965
console.log(Math.sqrt(64)); // 8
console.log(Math.abs(-55)); // 55
console.log(Math.PI); // 3.141592653589793
console.log(Math.E); // 2.718281828459045
